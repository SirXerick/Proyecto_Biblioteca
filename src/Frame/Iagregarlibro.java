void verautor (String lidautor){
   String idautor="";
    String sql="Select * from autores where id='"+lidautor+"'";
            Statement st;
        try {
            st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);
        while(rs.next())
            {
             lidautor=rs.getString("Nombre");
            // turno=rs.getString("Turno");
            }
             idautor=lidautor;
         if (idautor.equals("")){
             JOptionPane.showMessageDialog(null, "No se encontro Resultado");
         }
         else {
         JOptionPane.showMessageDialog(null, "Autor verificado");
         btnagregar.setEnabled(true);
         btnverficar.setEnabled(false);
         }
        
        } catch (SQLException ex) {
            Logger.getLogger(IRegistrarP.class.getName()).log(Level.SEVERE, null, ex);
        }
}

