void agregarautor(String id, String Nombre, String Apellidos){
      String sql="";
    if (id.equals("") || Nombre.equals("")|| Apellidos.equals("")){
            JOptionPane.showMessageDialog(null, "no puede dejar campos vacios");   
            }
            else{                
            sql="INSERT INTO autores (id,nombre,apellido) VALUES (?,?,?)";
        try {
            PreparedStatement pst  = cn.prepareStatement(sql);
            pst.setString(1, id);
            pst.setString(2, Nombre);
            pst.setString(3, Apellidos);
            int n=pst.executeUpdate();
            if(n>0){
            JOptionPane.showMessageDialog(null, "se Registro el Autor con exito");
       
            }      
        } catch (SQLException ex) {
            Logger.getLogger(IRegistrarP.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "hubo un error en el registro");
            
        } 
}}
void agregarlibro(String id,String idautor, String titulo,String editorial,String edicion, String stock){ 
            String sql="";
            if (id.equals("") || idautor.equals("")|| titulo.equals("")|| editorial.equals("") || edicion.equals("") || stock.equals("")){
            JOptionPane.showMessageDialog(null, "no puede dejar campos vacios");   
            }
            else{                
            sql="INSERT INTO Libros (id,idautor,titulo,editorial,edicion,stock) VALUES (?,?,?,?,?,?)";
        try {
            PreparedStatement pst  = cn.prepareStatement(sql);
            pst.setString(1, id);
            pst.setString(2, idautor);
            pst.setString(3, titulo);
            pst.setString(4, editorial);
            pst.setString(5, edicion);
            pst.setString(6, stock);
            int n=pst.executeUpdate();
            if(n>0){
            JOptionPane.showMessageDialog(null, "Se registro el libro con exito");
         
            }      
        } catch (SQLException ex) {
            Logger.getLogger(IRegistrarP.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "hubo un error en el registro");
            
        }}

}
